    static void Main(string[] args)
    {
    	int x = 4;
    	Console.WriteLine("x++: {0}", x++); //after this statement x = 5
    	Console.WriteLine("++x: {0}", ++x); 
    
    	int y = 4;
    	Console.WriteLine("y--: {0}", y--); //after this statement y = 3
    	Console.WriteLine("--y: {0}", --y);
    
    	Console.ReadKey();
    }
