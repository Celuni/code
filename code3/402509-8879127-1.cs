    public class Autocomplete
    {
    
        private int val;
        private string text;
    
        public int Value
        {
            get
            {
                return val;
            }
            set
            {
    
                val = value;
            }
        }
    
        public string Text
        {
            get
            {
                return text;
            }
            set
            {
    
                text = value;
            }
        }
    }
