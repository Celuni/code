    using System;
    namespace SimpleMaths
    {
      public class Operations
      {
        public static int add(int a, int b)
        {
            return a + b;
        }
 
        public static int substract(int a, int b)
        {
            return a - b;
        }
 
        public static int multiply(int a, int b)
        {
            return a * b;
        }
      }
    }
