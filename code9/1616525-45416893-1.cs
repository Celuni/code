    public class BeforeData
    {
        public int ID;
        public string name, code;
    
        public BeforeData(int ID, string name, string code)
        {
            this.ID = ID;
            this.name = name;
            this.code = code;
        }
    }
    public class AfterData
    {
        public string name, code;
    
        public AfterData(string name, string code)
        {
            this.name = name;
            this.code = code;
        }
    }
