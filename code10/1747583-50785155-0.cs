    public static class Printer
    {
        public static void Print(string output, int numberOfTimes)
        {
            for (int i = 0; i < numberOfTimes; i++)
            {
                Console.WriteLine(output);
            }
        }
        public static void Show(string output, int numberOfTimes)
        {
            for (int i = 0; i < numberOfTimes; i++)
            {
                Console.WriteLine(output);
            }
        }
    }
