    {
        public override string ToString()
        {
            public override string ToString()
            {
            return string.Join(",",
                this.ReferenceNumber,
                this.FirstName,
                this.Surname,
                this.MiddleName,
                this.Address1,
                this.Address2,
                this.Address3,
                this.Address4,
                this.City,
                this.PostalCode,
                this.Country,
                this.DateOfBirth.ToString(),
                this.ID,
                this.Gender,
                this.Component,
                this.NationalID,
                this.SubSystemID,
                this.System);
            }
        }
    }
