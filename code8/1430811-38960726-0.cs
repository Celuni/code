    /// <summary>
    /// Class Data 
    /// </summary>
    public class DataClass
    {    
        /// <summary>
        /// Declare variable name
        /// </summary>
        private string name;
    
        /// <summary>
        /// Declare variable id
        /// </summary>
        private string id;
        /// <summary>
        ///  Gets or sets Id
        /// </summary>
        public string Id
        {
            get { return this.id; }
            set { this.id = value; }
        }
    
        /// <summary>
        ///  Gets or sets Name
        /// </summary>
        public string Name
        {
            get { return this.name; }
            set { this.name = value; }
        }
    }
