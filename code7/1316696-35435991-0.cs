    class EmployeeData {
        public int Id {get;}
        public string Name {get;}
        public int Dept {get;}
        public double Pay {get;}
        public double Hours {get;}
        public EmployeeData(int id, string name, int dept, double pay, double hours) {
            Id = id;
            Name = name;
            Dept = dept;
            Pay = pay;
            Hours = hours;
        }
    }
