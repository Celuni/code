            private string GenerateNickName()
            {
                this.NickNameAutoGenerated = true;
                string empty = string.Empty;
                if (!string.IsNullOrEmpty(this.FirstName) && !string.IsNullOrEmpty(this.LastName))
                {
                    empty = string.Concat(this.FirstName, " ", this.LastName);
                }
                else if (!string.IsNullOrEmpty(this.FirstName))
                {
                    empty = this.FirstName;
                }
                else if (!string.IsNullOrEmpty(this.LastName))
                {
                    empty = this.LastName;
                }
                else if (this.User.Email.Contains("@"))
                {
                    string email = this.User.Email;
                    char[] chrArray = new char[] { '@' };
                    empty = email.Split(chrArray).FirstOrDefault<string>();
                }
                if (empty.Length > 60)
                {
                    empty = empty.Substring(0, 60);
                }
                return empty;
            }
