    public class Person
        {
            private int Age;
            private string Name;
            private string HairColour;
    
    
            public Person(int theAge)
            {
               Age = theAge;
            }
    
            public Person(int theAge, string theName)
            {
                Age = theAge;
                Name = theName;
            }
    
            public Person(int theAge, string theName, string theHairColour)
            {
                Age = theAge;
                Name = theName;
                HairColour = theHairColour;
            }
    
        }
