          public enum pluginType
          {
            /// <summary>
            /// Plugin is an unknown type (Default), plugins set to this will NOT be loaded
            /// </summary>
            unknown = -1,
            /// <summary>
            /// Plugin is a printer driver
            /// </summary>
            printer,
            /// <summary>
            /// Plugin is a scanner driver
            /// </summary>
            scanner,
            /// <summary>
            /// Plugin is a digital camera driver
            /// </summary>
            digitalCamera,
          }
